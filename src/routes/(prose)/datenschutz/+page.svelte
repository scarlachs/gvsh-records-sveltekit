<h2 id="auftragsverarbeitungsvertrag-avv">Auftragsverarbeitungsvertrag (AVV)</h2>
<p>
	In diesem Abschnitt möchten wir Ihnen erklären, was ein Auftragsverarbeitungsvertrag ist und
	warum dieser benötigt wird. Weil das Wort &bdquo;Auftragsverarbeitungsvertrag&rdquo; ein
	ziemlicher Zungenbrecher ist, werden wir hier im Text auch öfters nur das Akronym AVV benutzen.
	Wie die meisten Unternehmen arbeiten wir nicht alleine, sondern nehmen auch selbst
	Dienstleistungen anderer Unternehmen oder Einzelpersonen in Anspruch. Durch die Einbeziehung
	verschiedener Unternehmen bzw. Dienstleister kann es sein, dass wir personenbezogene Daten zur
	Verarbeitung weitergeben. Diese Partner fungieren dann als Auftragsverarbeiter, mit denen wir
	einen Vertrag, den sogenannten Auftragsverarbeitungsvertrag (AVV), abschließen. Für Sie am
	wichtigsten zu wissen ist, dass die Verarbeitung Ihrer personenbezogenen Daten ausschließlich
	nach unserer Weisung erfolgt und durch den AVV geregelt werden muss.
</p>
<h3>Wer sind Auftragsverarbeiter?</h3>
<p>
	Wir sind als Unternehmen und Websiteinhaber für alle Daten, die wir von Ihnen verarbeiten
	verantwortlich. Neben den Verantwortlichen kann es auch sogenannte Auftragsverarbeiter geben.
	Dazu zählt jedes Unternehmen bzw. jede Person, die in unserem Auftrag personenbezogene Daten
	verarbeitet. Genauer und nach der DSGVO-Definition gesagt: jede natürliche oder juristische
	Person, Behörde, Einrichtung oder eine andere Stelle, die in unserem Auftrag personenbezogene
	Daten verarbeitet, gilt als Auftragsverarbeiter. Auftragsverarbeiter können folglich
	Dienstleister wie Hosting- oder Cloudanbieter, Bezahlungs- oder Newsletter-Anbieter oder große
	Unternehmen wie beispielsweise Google oder Microsoft sein.
</p>
<p>
	Zur besseren Verständlichkeit der Begrifflichkeiten hier ein Überblick über die drei Rollen in
	der DSGVO:
</p>
<p>
	<strong>Betroffener</strong>(Sie als Kunde oder Interessent) → <strong>Verantwortlicher</strong>
	(wir als Unternehmen und Auftraggeber) → <strong>Auftragsverarbeiter</strong> (Dienstleister wie
	z. B. Webhoster oder Cloudanbieter)
</p>
<h3>Inhalt eines Auftragsverarbeitungsvertrages</h3>
<p>
