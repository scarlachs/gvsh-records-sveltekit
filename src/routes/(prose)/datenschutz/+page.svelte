<h2 id="tls-verschluesselung-https">TLS-Verschlüsselung mit https</h2>
<p>
	TLS, Verschlüsselung und https klingen sehr technisch und sind es auch. Wir verwenden HTTPS (das
	Hypertext Transfer Protocol Secure steht für &bdquo;sicheres
	Hypertext-Übertragungsprotokoll&rdquo;), um Daten abhörsicher im Internet zu übertragen.<br />
	Das bedeutet, dass die komplette Übertragung aller Daten von Ihrem Browser zu unserem Webserver abgesichert
	ist &ndash; niemand kann &bdquo;mithören&rdquo;.
</p>
<p>
	Damit haben wir eine zusätzliche Sicherheitsschicht eingeführt und erfüllen den Datenschutz
	durch Technikgestaltung (<a
		href="https://eur-lex.europa.eu/legal-content/DE/TXT/HTML/?uri=CELEX:32016R0679&from=DE&tid=312746961"
		target="_blank"
		rel="noopener">Artikel 25 Absatz 1 DSGVO</a
	>). Durch den Einsatz von TLS (Transport Layer Security), einem Verschlüsselungsprotokoll zur
	sicheren Datenübertragung im Internet, können wir den Schutz vertraulicher Daten sicherstellen.<br
	/>
	Sie erkennen die Benutzung dieser Absicherung der Datenübertragung am kleinen Schlosssymbol <LockClosed
		className="inline-flex size-4"
	/> links oben im Browser, links von der Internetadresse (z. B. beispielseite.de) und der Verwendung
	des Schemas https (anstatt http) als Teil unserer Internetadresse.<br />
	Wenn Sie mehr zum Thema Verschlüsselung wissen möchten, empfehlen wir die Google Suche nach &bdquo;Hypertext
	Transfer Protocol Secure wiki&rdquo; um gute Links zu weiterführenden Informationen zu erhalten.
</p>
