	einen AVV abgeschlossen. Darin wird allen voran festgehalten, dass der Auftragsverarbeiter die
	zu bearbeitenden Daten ausschließlich gemäß der DSGVO verarbeitet. Der Vertrag muss schriftlich
	abgeschlossen werden, allerdings gilt in diesem Zusammenhang auch der elektronische
	Vertragsabschluss als „schriftlich“. Erst auf der Grundlage des Vertrags erfolgt die
